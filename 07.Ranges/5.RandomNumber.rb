puts rand #gives a random number between 0 and 1

puts rand.round(2) #rounds the given number to 2 digits

puts rand(100) #gives a random number(integer) between 0 to 99 upto 100 but not 100

puts rand * 100 #gives a random number decimal between 0 and 100 not 100
puts rand(50...60)# gives a random number between 0 and 50 to 60 60 not inlcuded
puts rand(50..60)# gives a random number between 0 and 50 to 60 60 included

